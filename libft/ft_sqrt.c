#include "libft.h"

int ft_sqrt(int n)
{
	int i;

	if (n <= 0)
		return (0);
	i = 1;
	while (i * i < n && i <= 2147483647 / i)
		i++;
	if (i * i == n)
		return (i);
	return (i - 1);
}